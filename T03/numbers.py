#create three variables to store input function for user to enter 3 different integers
print("Please enter 3 different integers using the inputs below: ")
num1 = int(input("Enter your first integer "))
num2 = int(input("Enter your second integer "))
num3 = int(input("Enter your third integer "))

#create a variable to store the sum of the numbers

number_sum = (num1 + num2 + num3)

#calculate and display the sum of numbers
print ("The sum of the 3 integers =", number_sum)

#calculate and display first number minus the second number
print("The first number minus the second number =", num1 - num2)

#calculate and display the third number multiplied by the first number
print("The third number multiplied by the first number =", num3 * num1)

#calculate and display the number_sum divided by num3
print("The sum of all the three numbers divided by the third number =", number_sum / num3)
    